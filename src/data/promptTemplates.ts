// Helper function to get template by ID
export function getPromptTemplateById(id: string): PromptTemplate | undefined {
  return promptTemplates.find(t => t.id === id);
}

// Helper function to get templates by category
export function getPromptTemplatesByCategory(category: PromptTemplate['category']): PromptTemplate[] {
  return promptTemplates.filter(t => t.category === category);
}

// Helper function to search templates
export function searchPromptTemplates(query: string): PromptTemplate[] {
  const lowerQuery = query.toLowerCase();
  return promptTemplates.filter(t =>
    t.title.toLowerCase().includes(lowerQuery) ||
    t.description.toLowerCase().includes(lowerQuery) ||
    t.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    t.content.toLowerCase().includes(lowerQuery)
  );
}

