// Helper to find policy by slug
export function findPolicyBySlug(slug: string): UniversityPolicy | undefined {
  return policiesData.find(p => p.id === slug);
}

// Get all unique countries
export function getAllCountries(): string[] {
  return [...new Set(policiesData.map(p => p.country))].sort();
}

// Get all unique regions
export function getAllRegions(): string[] {
  return [...new Set(policiesData.map(p => p.region))].sort();
}

// Get policies by country
export function getPoliciesByCountry(country: string): UniversityPolicy[] {
  return policiesData.filter(p => p.country === country);
}

// Get policies by region
export function getPoliciesByRegion(region: string): UniversityPolicy[] {
  return policiesData.filter(p => p.region === region);
}

// Get policies by overall policy type
export function getPoliciesByPolicyType(policyType: UniversityPolicy['overallPolicy']): UniversityPolicy[] {
  return policiesData.filter(p => p.overallPolicy === policyType);
}

// Get top N universities by QS ranking
export function getTopByQSRank(limit: number = 10): UniversityPolicy[] {
  return policiesData
    .filter(p => p.qsRank !== null)
    .sort((a, b) => (a.qsRank || 0) - (b.qsRank || 0))
    .slice(0, limit);
}

// Search policies by name
export function searchPolicies(query: string): UniversityPolicy[] {
  const lowerQuery = query.toLowerCase();
  return policiesData.filter(p =>
    p.universityName.toLowerCase().includes(lowerQuery) ||
    p.universityNameEn.toLowerCase().includes(lowerQuery) ||
    p.country.toLowerCase().includes(lowerQuery) ||
    p.countryEn.toLowerCase().includes(lowerQuery)
  );
}

// Get all allowed tools across all policies
export function getAllAllowedTools(): string[] {
  const tools = new Set<string>();
  policiesData.forEach(p => {
    p.allowedTools.forEach(t => tools.add(t));
  });
  return [...tools].sort();
}

// Get policies by university type (water/regular)
export function getPoliciesByType(type: 'water' | 'regular' | 'all' = 'all'): UniversityPolicy[] {
  // Water programs are identified by: null ranks, certain naming patterns, or explicit marking
  const waterProgramIds = [
    'university-of-london', 'western-sydney', 'liverpool-john-moores',
    'portsmouth', 'deakin', 'gold-coast', 'canberra', 'northumbria'
  ];

  if (type === 'water') {
    return policiesData.filter(p => waterProgramIds.includes(p.id));
  } else if (type === 'regular') {
    return policiesData.filter(p => !waterProgramIds.includes(p.id));
  }
  return policiesData;
}

// Get policies with null ranks (often indicator of less mainstream institutions)
export function getUnrankedPolicies(): UniversityPolicy[] {
  return policiesData.filter(p => p.qsRank === null);
}

// Get policies grouped by country
export function getPoliciesGroupedByCountry(): Record<string, UniversityPolicy[]> {
  const grouped: Record<string, UniversityPolicy[]> = {};
  policiesData.forEach(p => {
    if (!grouped[p.country]) {
      grouped[p.country] = [];
    }
    grouped[p.country].push(p);
  });
  return grouped;
}

// Get related policies for a given policy
export function getRelatedPoliciesForPolicy(policy: UniversityPolicy, limit: number = 6): UniversityPolicy[] {
  return policiesData
    .filter(p => p.id !== policy.id)
    .map(p => ({
      ...p,
      relevanceScore: (
        (p.country === policy.country ? 3 : 0) +
        (p.region === policy.region ? 1 : 0) +
        (Math.abs((p.qsRank || 999) - (policy.qsRank || 999)) < 50 ? 2 : 0)
      )
    }))
    .filter(p => p.relevanceScore > 0)
    .sort((a, b) => b.relevanceScore - a.relevanceScore)
    .slice(0, limit)
    .map(({ relevanceScore, ...p }) => p);
}

